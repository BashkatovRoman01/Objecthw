public class Author {
    static String authorFirstName;

    static String authorSecondtName;


    public Author( String authorFirstName, String authorSecondtName) {
        this.authorFirstName = authorFirstName;

        this.authorSecondtName = authorSecondtName;
    }
    public String getAuthorFirstName() {
        return this.authorFirstName;
    }
    public String getAuthorSecondtName() {
        return this.authorSecondtName;
    }
}

